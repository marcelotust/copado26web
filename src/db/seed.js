export function buildStickerRows() {
  const rows = []
  for (const section of SECTIONS) {
    for (let n = 1; n <= section.count; n++) {
      rows.push({
        id: `${section.code}-${String(n).padStart(2, '0')}`,
        teamCode: section.code,
        number: n,
        quantity: 0
      })
    }
  }
  return rows
}
